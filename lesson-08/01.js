function capitalizeWords(str) {
    let result=''
    let capitalize=true
    for(let i=0;i<str.length;i++){
        if(capitalize && str[i]!==' '){
            result+=str[i].toUpperCase()
            capitalize=false
        } else{
            result+=str[i]
        }
        if(str[i]===' '){
            capitalize=true
        }
    }
    return result
}

